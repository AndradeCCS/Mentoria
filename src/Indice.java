public class Indice {
    private double indiceInicial;
    private double indiceFinal;

    public Indice(double indiceInicial, double indiceFinal) {
        this.indiceInicial = indiceInicial;
        this.indiceFinal = indiceFinal;
    }

    public double getIndiceInicial() {
        return indiceInicial;
    }

    public void setIndiceInicial(double indiceInicial) {
        this.indiceInicial = indiceInicial;
    }

    public double getIndiceFinal() {
        return indiceFinal;
    }

    public void setIndiceFinal(double indiceFinal) {
        this.indiceFinal = indiceFinal;
    }
}
